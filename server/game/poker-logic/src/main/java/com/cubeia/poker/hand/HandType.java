package com.cubeia.poker.hand;

import com.cubeia.poker.handhistory.api.BestHandType;

public enum HandType {
    NOT_RANKED,
    HIGH_CARD,
    PAIR,
    TWO_PAIRS,
    THREE_OF_A_KIND,
    STRAIGHT,
    FLUSH,
    FULL_HOUSE,
    FOUR_OF_A_KIND,
    STRAIGHT_FLUSH,
    ROYAL_STRAIGHT_FLUSH;

    public BestHandType translate() {
        return BestHandType.values()[ordinal()];
    }
}
